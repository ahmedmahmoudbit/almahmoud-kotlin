private val HTML_TAG_REGEX = Regex("<[^>]*>")

fun Int.toArabicNumerals(): String =
    this.toString().map { ARABIC_DIGITS[it - '0'] }.joinToString("")

fun getSurahNameArabic(surahNo: Int): String =
    SURAH_NAMES_ARABIC.getOrElse(surahNo) { "" }

fun stripHtml(text: String): String =
    text.replace(HTML_TAG_REGEX, "").trim()
